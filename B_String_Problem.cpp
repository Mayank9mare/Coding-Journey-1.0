int solve(){
    string s1,s2;
    cin>>s1>>s2;
    int n;
    cin>>n;
    vector<vector<ll>> d;
    d.assign(26,vector<ll>(26,INT_MAX));
    for(int i=0;i<26;i++){
        d[i][i]=0;
    }
    for(int i=0;i<n;i++){
        char x,y;
        ll w;
        cin>>x>>y>>w;
        d[x-'a'][y-'a']=min(w,d[x-'a'][y-'a']);

    }
    for(int i=0;i<26;i++){
        for(int j=0;j<26;j++){
            for(int l=0;l<26;l++){
                if(d[j][l]>d[j][i]+d[i][l]){
                    d[j][l]=d[j][i]+d[i][l];
                }
            }
        }
    }
    if(s1.size()!=s2.size()){
        cout<<-1<<endl;
        return 0;
    }
    int ans=0;
    string k="";
    for(int i=0;i<s1.size();i++){
        int x=s1[i]-'a';
        int y=s2[i]-'a';
        ll p=INT_MAX;
        char c='*';
        for(int j=0;j<26;j++){
            if((d[x][j]+d[y][j])<p){
                p=d[x][j]+d[y][j];
                c=char(int('a')+j);

            }
        }

        if(c=='*'){
            cout<<-1<<endl;
            return 0;

        }
        k+=c;
        ans+=p;
    }
    cout<<ans<<endl;
    cout<<k<<endl;

    return 0;

}


