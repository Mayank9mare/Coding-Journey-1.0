//KnightMareVoid
int n;
vector<vector<int>> adj;
int sum;
vector<int> sub;
void dfs(int u,int p){
    sub[u]=1;
    for(int x:adj[u]){
        if(x==p)continue;
        dfs(x,u);
        sub[u]+=sub[x];
    }
}
void dfs2(int u,int p,int & ans){
    ans=max(ans,sum);
    for(int x:adj[u]){
        if(x==p)continue;
        //reroot
        int root=sub[u];
        int child=sub[x];
        int prev=sum;
        sub[u]=sub[u]-sub[x];
        sub[x]=n;
        sum-=(root+child);
        sum+=(sub[u]+sub[x]);
        dfs2(x,u,ans);
        sum=prev;
        sub[u]=root;
        sub[x]=child;


    }

}
int solve(){
    //int n;
    cin>>n;
    sum=0;
    adj.assign(n,vector<int>(0));
    sub.assign(n,0);
    for(int i=0;i<n-1;i++){
        int x,y;
        cin>>x>>y;
        x--;y--;
        adj[x].pb(y);
        adj[y].pb(x);
    }
    dfs(0,-1);
    for(int i=0;i<n;i++)sum+=sub[i];
    int ans=sum;
    dfs2(0,-1,ans);
    cout<<ans<<endl;

    return 0;

}


