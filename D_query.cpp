//KnightMareVoid
struct qry{
    int l,r,i,bno;

};
bool cmp(qry a,qry b){
    if(a.bno==b.bno){
        //if(a.bno&1)return a.r>b.r;
        return a.r<b.r;
    }
    else{
        return a.bno<b.bno;
    }
}
int freq[1000005];
int n;
int tot=0;
void add(int x){
    //if(x>n)return;
    // if(freq[x]==1)tot--;
    freq[x]++;
    if(freq[x]==1)tot++;

}
void remove(int x){
    //if(x>n)return;
    //if(freq[x]==1)tot--;
    freq[x]--;
    if(freq[x]==0)tot--;
}
int solve(){
    int q;
    cin>>n;
    int a[n];
    int b=sqrt(n);
    for(int i=0;i<n;i++)cin>>a[i];
    cin>>q;
    vector<qry> v(q);
    for(int i=0;i<q;i++){
        cin>>v[i].l>>v[i].r;
        v[i].l--;v[i].r--;
        v[i].i=i;
        v[i].bno=v[i].l/b;
        
    }

    sort(all(v),cmp);
    //intilalise
    int l=0,r=0;
    add(a[0]);
    int ans[q];
    for(int i=0;i<q;i++){
        int curL=v[i].l;
        int curR=v[i].r;
        while(curL<l){
            --l;
            add(a[l]);
            
        }
        while(curR>r){
            ++r;
            add(a[r]);
            //r++;
        }
        while(curL>l){
            remove(a[l]);
            l++;
        }
        while(curR<r){
            remove(a[r]);
            r--;
        }
        ans[v[i].i]=tot;

    }
    for(int i=0;i<q;i++){
        cout<<ans[i]<<endl;
    }
    return 0;

}



