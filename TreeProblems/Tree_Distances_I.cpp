// For each node, we want to calculate maximum distance to another node. 
// Previously we saw that that if we start a BFS from any node, 
// we end up on either of the diametric end. We can use this fact to efficiently compute the answer. 
// Lets calculate distances of each node from both the ends of the diameter. Then maximum distance of each node can be calculated as:

// max_distance[u] = max(distance_from_diametric_end1[u], distance_from_diametric_end2[u])
vector<int> d;
pll bfs(int s){
    queue<pll> q;
    q.push({s,0});
    int n=adj.size();
    vector<int> v(n,0);
    v[s]=1;
    pll l={s,0};
    while(q.size()){
        int p=q.front().first;
        int r=q.front().second;
        // debug(p);
        // debug(r);
        
        q.pop();
        l={p,r};
        d[p]=max(d[p],r);
        //cout<<r<<endl;
    
        //v[p]=1;
        for(int x:adj[p]){
            if(v[x]==0){
                v[x]=1;
                
                q.push({x,r+1});
            }
        }



    }
    return l;

}
