void dj(int s,vector<array<int,2>> &d){
   
    d.resize(n);
    for(int i=0;i<n;i++){
        d[i][0]=LONG_LONG_MAX;
        d[i][1]=LONG_LONG_MAX;
    }
    d[s][0]=0;
    d[s][1]=0;
    //cout<<1<<endl;
    priority_queue<pll,vector<pll>,greater<pll>> pq;
    pq.push({0,s,0});
    while(pq.size()){
        int p=pq.top()[1];
        int c=pq.top()[0];
        int t=pq.top()[2];
        //cout<<p<<sp<<t<<endl;
        pq.pop();
        if(d[p][t]!=c)continue;
        if(t==1){
            for(auto x:adj[p]){
                int l=x.second;
                int v=x.first;
                if(l+c<d[v][1]){
                    d[v][1]=l+c;
                    pq.push({l+c,v,1});
                }
            }
        }
        else{
            for(auto x:adj[p]){
                int l=x.second;
                int v=x.first;
                if(l+c<d[v][0]){
                    d[v][0]=l+c;
                    pq.push({l+c,v,0});
                }
                int l2=l/2;
                if(l2+c<d[v][1]){
                    d[v][1]=l2+c;
                    pq.push({l2+c,v,1});
                }


            }
        }


    }
}
